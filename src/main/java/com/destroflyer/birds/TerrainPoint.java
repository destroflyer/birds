package com.destroflyer.birds;

/**
 *
 * @author Carl
 */
public class TerrainPoint {

    public TerrainPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }
    private int x;
    private int y;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
